export function getLegalPage(key: LegalPageKey): LegalPageContent {
  return LEGAL_PAGES[key];
}

export function allLegalPagePaths(): string[] {
  return LEGAL_PAGE_KEYS.map((key) => LEGAL_PAGES[key].path);
}

export function legalPageMetadata(key: LegalPageKey): Metadata {
  const page = getLegalPage(key);
  const base = (SITE.siteUrl || `https://${S.siteHost}`).replace(/\/$/, "");
  const canonical = `${base}${page.path}`;

  return {
    title: page.title,
    description: page.description,
    alternates: { canonical },
    robots: { index: true, follow: true },
  };
}
